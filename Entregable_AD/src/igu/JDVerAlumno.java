    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        // TODO add your handling code here:
        String nombreBuscar=txtNombreBuscar.getText();
        int horasTotales = 0;
        
       Alumno alumnoSeleccionado=null;
        for(Alumno alu:alumnos){
            if(alu.getNombre().equalsIgnoreCase(nombreBuscar)){
                alumnoSeleccionado = alu;
                break;
            }
        }
        if(alumnoSeleccionado != null){
            txtNombreAlumno1.setText(alumnoSeleccionado.getNombre()+ "\n");
            txtLocalidad.setText(alumnoSeleccionado.getLocalidad()+ "\n");
            txtModulos.setText(alumnoSeleccionado.getInfo()+ "\n");
           
            for(Modulo mod : modulos){
                horasTotales += mod.getHoras();
                txtHoras.setText(Integer.toString(horasTotales)+ "\n");
                
            }
            
        }else{
            JOptionPane.showMessageDialog(this, "alumno no se encontró.", "error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnMostrarActionPerformed

