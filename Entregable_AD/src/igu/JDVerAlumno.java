         ArrayList<Alumno> listaAlumnos = new ArrayList<>();
                 
        for(Alumno alumno:listaAlumnos){
            if(alumno.getNombre().equalsIgnoreCase(nombre)){
                return alumno;
            }
        }
        return null; //si no se encuentra
        
        
